"""External benchmark — FUT-K's rating models vs bookmaker odds (1X2).

Downloads real results + Bet365 closing odds for a league season from
football-data.co.uk and scores four match-outcome predictors walk-forward
(every prediction uses only matches that finished earlier):

  1. trailing H/D/A frequencies  (the naive baseline)
  2. Elo                         (classic rating model)
  3. attack/defense Poisson      (the engine's Poisson machinery, team-level)
  4. bookmaker implied           (de-margined Bet365 odds — the market)

This anchors the project externally (validation/README.md §7): if our simple
models sit between the naive baseline and the market, the machinery is sane;
nobody honest expects to beat the closing line with public data.
"""

from __future__ import annotations

import argparse
import csv
import io
import os
import urllib.request

from fie.ratings import (
    Elo,
    PoissonAD,
    TrailingFrequencies,
    brier_multi,
    implied_1x2,
    logloss_multi,
    outcome_index,
)

FD_URL = "https://www.football-data.co.uk/mmz4281/{season}/{league}.csv"
DEFAULT_CACHE = os.path.join(
    os.path.dirname(os.path.dirname(os.path.abspath(__file__))), ".sb_cache"
)


def load_rows(league: str, season: str, cache_dir: str) -> list:
    """Download (or read cached) football-data.co.uk CSV; keep usable rows."""
    os.makedirs(cache_dir, exist_ok=True)
    path = os.path.join(cache_dir, f"fd_{league}_{season}.csv")
    if not os.path.exists(path):
        with urllib.request.urlopen(FD_URL.format(season=season, league=league), timeout=60) as r:
            data = r.read()
        with open(path, "wb") as fh:
            fh.write(data)
    with open(path, "rb") as fh:
        text = fh.read().decode("latin-1")
    rows = []
    for row in csv.DictReader(io.StringIO(text)):
        try:
            rows.append(
                {
                    "date": row["Date"],
                    "home": row["HomeTeam"].strip(),
                    "away": row["AwayTeam"].strip(),
                    "hg": int(row["FTHG"]),
                    "ag": int(row["FTAG"]),
                    "oh": float(row["B365H"]),
                    "od": float(row["B365D"]),
                    "oa": float(row["B365A"]),
                }
            )
        except (KeyError, ValueError):
            continue  # blank tail rows / missing odds
    return rows


def main():
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--league", default="SP1", help="football-data code (SP1 = La Liga)")
    ap.add_argument("--season", default="1516")
    ap.add_argument("--burn-in", type=int, default=60, help="matches used only to warm ratings")
    ap.add_argument("--cache", default=DEFAULT_CACHE)
    ap.add_argument("--out", default="validation/results/RESULTS_BENCHMARK.md")
    args = ap.parse_args()

    rows = load_rows(args.league, args.season, args.cache)
    print(f"{args.league} {args.season}: {len(rows)} matches with results + Bet365 odds")

    base = TrailingFrequencies()
    elo = Elo()
    pad = PoissonAD()
    models = {
        "Trailing frequencies (naive)": [],
        "Elo": [],
        "Attack/defense Poisson": [],
        "Bookmaker implied (Bet365)": [],
    }

    scored = 0
    for i, m in enumerate(rows):
        # Predict first (walk-forward: only past matches inform the models) ...
        if i >= args.burn_in:
            preds = {
                "Trailing frequencies (naive)": base.predict_1x2(),
                "Elo": elo.predict_1x2(m["home"], m["away"], base.draw_rate),
                "Attack/defense Poisson": pad.predict_1x2(m["home"], m["away"]),
                "Bookmaker implied (Bet365)": implied_1x2(m["oh"], m["od"], m["oa"]),
            }
            idx = outcome_index(m["hg"], m["ag"])
            for name, probs in preds.items():
                models[name].append((brier_multi(probs, idx), logloss_multi(probs, idx)))
            scored += 1
        # ... then update with the result.
        base.update(m["hg"], m["ag"])
        elo.update(m["home"], m["away"], m["hg"], m["ag"])
        pad.update(m["home"], m["away"], m["hg"], m["ag"])

    lines = [
        "# RESULTS (benchmark) — external anchor: ratings vs bookmaker odds",
        "",
        f"> Generated by `scripts/benchmark_external.py` on football-data.co.uk "
        f"{args.league} {args.season} ({scored} matches scored after a "
        f"{args.burn_in}-match burn-in). Walk-forward 1X2 (home/draw/away): every "
        "prediction uses only earlier matches. Multiclass Brier and log loss; "
        "lower is better.",
        "",
        "| Predictor | Brier (1X2) | Log loss (1X2) |",
        "|---|---|---|",
    ]
    for name, pairs in models.items():
        n = len(pairs)
        b = sum(p[0] for p in pairs) / n
        ll = sum(p[1] for p in pairs) / n
        lines.append(f"| {name} | {b:.4f} | {ll:.4f} |")

    lines += [
        "",
        "## Reading",
        "",
        "- The **bookmaker line is the ceiling**: closing odds aggregate vastly "
        "more information than public match results; matching them with public "
        "data alone would be extraordinary, and we do not claim it.",
        "- The honest test for FUT-K's machinery is the ordering: **naive baseline "
        "< our rating models <= market**. If our Elo / attack-defense Poisson sit "
        "between the naive baseline and the market, the probabilistic machinery is "
        "sane and externally anchored.",
        "- These are *match-outcome* models built on the engine's Poisson core; the "
        "in-play panel (goal-in-window) has no public baseline to compare against, "
        "which is exactly why this external anchor uses the 1X2 target.",
        "",
    ]
    with open(args.out, "w", encoding="utf-8") as fh:
        fh.write("\n".join(lines))
    print("\n".join(lines[4:10]))
    print(f"\nWrote {args.out}")


if __name__ == "__main__":
    main()
